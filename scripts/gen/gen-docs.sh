#!/usr/bin/env bash
# tools/gen-docs.sh
#
# Build the oline binary and generate docs/cli-reference.md from live --help
# and --examples output. Safe to run at any time — never modifies source code.
#
# Usage:
#   ./tools/gen-docs.sh               # writes docs/cli-reference.md
#   OLINE_BIN=./target/release/oline ./tools/gen-docs.sh   # use pre-built binary

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
OUT="$REPO_ROOT/docs/CLI-REFERENCE.md"
BIN="${OLINE_BIN:-}"

# ── 1. Locate or build the binary ────────────────────────────────────────────
if [[ -z "$BIN" ]]; then
    echo "==> Building oline (debug) ..."
    cargo build --manifest-path "$REPO_ROOT/Cargo.toml" 2>&1
    BIN="$REPO_ROOT/target/debug/oline"
fi

if [[ ! -x "$BIN" ]]; then
    echo "ERROR: binary not found or not executable: $BIN" >&2
    exit 1
fi

VERSION="$("$BIN" --version 2>/dev/null || echo "unknown")"
echo "==> Generating docs from $BIN ($VERSION)"

# ── 2. Helpers ───────────────────────────────────────────────────────────────
section() {
    printf '\n---\n\n## %s\n\n' "$1"
}

fence() {
    # wrap stdin in a fenced code block
    printf '```\n'
    cat
    printf '```\n'
}

run_help() {
    "$BIN" "$@" --help 2>&1 || true
}

run_examples() {
    "$BIN" "$@" --examples 2>&1 || true
}

# ── 3. Write header ──────────────────────────────────────────────────────────
mkdir -p "$(dirname "$OUT")"
{
cat <<HEADER
# O-Line CLI Reference

> Auto-generated by \`tools/gen-docs.sh\` — do **not** edit by hand.
> Regenerate with: \`just gen-docs\`

**Version:** $VERSION
**Generated:** $(date -u '+%Y-%m-%d %H:%M UTC')

## Top-level help

HEADER

run_help | fence

# ── 4. Subcommand loop ────────────────────────────────────────────────────────
# Format: "SubcommandName:cli-name[:has-examples]"
CMDS=(
    "Encrypt:encrypt:examples"
    "Deploy:deploy:examples"
    "SDL:sdl:examples"
    "Init:init:examples"
    "Manage:manage:examples"
    "Test S3:test-s3:examples"
    "Test gRPC:test-grpc:examples"
    "DNS:dns:examples"
    "Bootstrap:bootstrap:examples"
    "Sites:sites:examples"
    "Refresh:refresh:examples"
    "Node:node:examples"
    "Firewall:firewall:examples"
    "Relayer:relayer:examples"
    "VPN:vpn:examples"
    "Providers:providers:examples"
    "Registry:registry"
    "Testnet Deploy:testnet-deploy:examples"
    "Endpoints:endpoints:examples"
)

for entry in "${CMDS[@]}"; do
    IFS=: read -r label cmd has_examples <<< "$entry"

    section "$label (\`oline $cmd\`)"

    echo "### Usage"
    echo ""
    run_help "$cmd" | fence
    echo ""

    if [[ "$has_examples" == "examples" ]]; then
        # Check if --examples actually produces non-empty output
        examples_out="$(run_examples "$cmd")"
        if [[ -n "$examples_out" ]]; then
            echo "### Examples"
            echo ""
            printf '%s\n' "$examples_out" | fence
            echo ""
        fi
    fi
done

} > "$OUT"

echo "==> Written: $OUT"
wc -l "$OUT" | awk '{print "    " $1 " lines"}'

#!/usr/bin/env bash
# Generate Mermaid code diagrams from trailmark analysis.
# Usage: ./scripts/gen/gen-diagrams.sh [--threshold N]
#
# Requires: trailmark installed in .venv (uv pip install trailmark)
# Output:   docs/trailmark-analysis/diagrams/*.md
set -euo pipefail

# Parse --threshold N or plain N
THRESHOLD="15"
while [[ $# -gt 0 ]]; do
    case "$1" in
        --threshold) THRESHOLD="$2"; shift 2 ;;
        [0-9]*) THRESHOLD="$1"; shift ;;
        *) shift ;;
    esac
done
OUT_DIR="docs/trailmark-analysis/diagrams"
DIAGRAM_SCRIPT="$(find ~/.claude/plugins/cache -path '*/diagramming-code/scripts/diagram.py' 2>/dev/null | head -1)"

if [ -z "$DIAGRAM_SCRIPT" ]; then
    echo "Error: trailmark diagramming-code plugin not found."
    echo "Install via: /plugin marketplace add trailofbits/skills"
    exit 1
fi

# Activate venv
if [ -f .venv/bin/activate ]; then
    source .venv/bin/activate
else
    echo "Error: .venv not found. Run: uv venv && uv pip install trailmark"
    exit 1
fi

# Verify trailmark
if ! python3 -c "import trailmark" 2>/dev/null; then
    echo "Error: trailmark not installed. Run: uv pip install trailmark"
    exit 1
fi

mkdir -p "$OUT_DIR"

generate() {
    local fname="$1" title="$2" dtype="$3"
    shift 3
    local args=("$@")

    echo "  Generating $fname..."
    local mermaid
    mermaid="$(uv run "$DIAGRAM_SCRIPT" -t src/ -l rust -T "$dtype" "${args[@]}" 2>/dev/null \
        | grep -v '^Installed \|^Resolved \|^Prepared \|^Downloading \|^Downloaded \|^ Downloaded\|^Audited \|^ +' || true)"

    if [ -z "$mermaid" ] || echo "$mermaid" | tail -1 | grep -q '^No '; then
        echo "    SKIP: no data for $dtype"
        return
    fi

    local node_count edge_count
    node_count="$(echo "$mermaid" | grep -c '\["' || true)"
    edge_count="$(echo "$mermaid" | grep -c ' --> ' || true)"

    cat > "$OUT_DIR/$fname" <<MDEOF
# $title

Generated by \`trailmark --type $dtype ${args[*]}\`

\`\`\`mermaid
$mermaid
\`\`\`
MDEOF

    echo "    OK: $node_count nodes, $edge_count edges"
}

echo "==> Generating trailmark diagrams (threshold=$THRESHOLD)"

generate "01-deploy-orchestrator.md" \
    "deploy_all_units Orchestrator" \
    "call-graph" -f deploy_all_units -d 1

generate "02-deploy-full-callgraph.md" \
    "deploy_all_units Full Call Graph" \
    "call-graph" -f deploy_all_units -d 2

generate "03-main-entry.md" \
    "main() Entry Point" \
    "call-graph" -f main -d 1

generate "04-crypto-callgraph.md" \
    "SSH/Crypto Call Graph" \
    "call-graph" -f push_pre_start_files -d 2

generate "05-deployer-callgraph.md" \
    "Provider Selection" \
    "call-graph" -f interactive_select_provider -d 1

generate "06-complexity-heatmap.md" \
    "Complexity Heatmap (CC >= $THRESHOLD)" \
    "complexity" --threshold "$THRESHOLD"

generate "07-class-hierarchy.md" \
    "Struct/Trait Hierarchy" \
    "class-hierarchy"

generate "08-containment.md" \
    "Struct Containment" \
    "containment"

echo "==> Done. Diagrams in $OUT_DIR/"
ls -1 "$OUT_DIR/"

#!/usr/bin/env python3
"""Generate docs/cli-reference.md from oline --help + tagged example files.

Replaces the inline bash gen-docs recipe.  Reads YAML frontmatter from
docs/examples/*.md to inject examples into each CLI subcommand section and
appends a Justfile Recipes section for just-only commands.

Also writes docs/examples/_index.yml — a flat command→file lookup.
"""

import os
import re
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent.parent
EXAMPLES_DIR = ROOT / "docs" / "examples"
OUTPUT = ROOT / "docs" / "cli-reference.md"
INDEX_FILE = EXAMPLES_DIR / "_index.yml"
def _resolve_binary() -> Path:
    """Pick the oline binary to introspect.

    Priority:
      1. `OLINE_BIN` env var (absolute path or repo-relative).
      2. `target/release/oline` — what `just install` builds.
      3. `target/debug/oline` — `just build` fallback.
      4. `~/.cargo/bin/oline` — installed copy.
    """
    env = os.environ.get("OLINE_BIN")
    if env:
        p = Path(env)
        return p if p.is_absolute() else (ROOT / p).resolve()
    for rel in (Path("target/release/oline"), Path("target/debug/oline")):
        candidate = ROOT / rel
        if candidate.exists():
            return candidate
    cargo_home = Path(os.environ.get("CARGO_HOME", Path.home() / ".cargo"))
    return cargo_home / "bin" / "oline"


BINARY = _resolve_binary()


# ── YAML frontmatter parser (no PyYAML dependency) ──────────────────────────

def parse_frontmatter(text: str):
    """Return (commands, tags, body) from a markdown file with YAML frontmatter."""
    if not text.startswith("---"):
        return [], [], text

    parts = text.split("---", 2)
    if len(parts) < 3:
        return [], [], text

    meta = parts[1]
    body = parts[2].lstrip("\n")

    commands = []
    tags = []
    for line in meta.splitlines():
        line = line.strip()
        if line.startswith("- "):
            val = line[2:].strip()
            # Determine which list we're appending to based on recent header
            if _last_key == "commands":
                commands.append(val)
            elif _last_key == "tags":
                # tags might be inline [a, b] or list items
                tags.append(val)
        elif line.startswith("commands:"):
            _set_last_key("commands")
            inline = line.split(":", 1)[1].strip()
            if inline:
                commands.extend(_parse_inline_list(inline))
        elif line.startswith("tags:"):
            _set_last_key("tags")
            inline = line.split(":", 1)[1].strip()
            if inline:
                tags.extend(_parse_inline_list(inline))

    return commands, tags, body


_last_key = ""


def _set_last_key(k):
    global _last_key
    _last_key = k


def _parse_inline_list(s: str) -> list:
    """Parse '[a, b, c]' or 'a, b, c' into a list of strings."""
    s = s.strip("[] ")
    if not s:
        return []
    return [x.strip().strip("'\"") for x in s.split(",") if x.strip()]


# ── Load all example files ──────────────────────────────────────────────────

def load_examples():
    """Return dict: command_key → (filename, body)."""
    mapping = {}  # command → (filename, body)
    if not EXAMPLES_DIR.is_dir():
        return mapping

    for f in sorted(EXAMPLES_DIR.glob("*.md")):
        text = f.read_text()
        commands, _tags, body = parse_frontmatter(text)
        for cmd in commands:
            mapping[cmd] = (f.name, body)

    return mapping


# ── CLI help capture ────────────────────────────────────────────────────────

def run_help(args: list[str]) -> str:
    """Run oline with args and return stdout."""
    try:
        result = subprocess.run(
            [str(BINARY)] + args,
            capture_output=True, text=True, timeout=10,
        )
        return result.stdout
    except Exception as e:
        print(f"  Warning: failed to run {BINARY} {' '.join(args)}: {e}", file=sys.stderr)
        return ""


def get_subcommands(help_text: str) -> list[str]:
    """Extract subcommand names from --help output."""
    cmds = []
    for line in help_text.splitlines():
        m = re.match(r"^  ([a-z][-a-z0-9]*)\s", line)
        if m:
            cmds.append(m.group(1))
    return cmds


# ── Index generation ────────────────────────────────────────────────────────

def write_index(mapping):
    """Write docs/examples/_index.yml from the command→file mapping."""
    lines = ["# Auto-generated by scripts/gen-docs.py — do not edit", ""]
    for cmd in sorted(mapping.keys()):
        filename = mapping[cmd][0]
        lines.append(f"{cmd}: {filename}")
    lines.append("")
    INDEX_FILE.write_text("\n".join(lines))


# ── Main ────────────────────────────────────────────────────────────────────

def main():
    if not BINARY.exists():
        print(f"Error: {BINARY} not found. Run `cargo build` first.", file=sys.stderr)
        sys.exit(1)

    # Get version
    version = ""
    cargo_toml = ROOT / "Cargo.toml"
    if cargo_toml.exists():
        for line in cargo_toml.read_text().splitlines():
            if line.startswith("version"):
                version = line.split('"')[1] if '"' in line else ""
                break
    print(f"==> Generating docs from {BINARY} (oline {version})")

    # Load example files
    examples = load_examples()
    print(f"  Loaded {len(examples)} command→example mappings from {EXAMPLES_DIR}")

    # Capture CLI help
    main_help = run_help(["--help"])
    subcommands = get_subcommands(main_help)

    out = []

    # Header
    out.append("# o-line CLI Reference\n")
    out.append("## Usage\n")
    out.append("```")
    out.append(main_help.rstrip())
    out.append("```\n")

    # Available Commands
    out.append("## Subcommands\n")
    out.append("### Available Commands\n")
    out.append("```")
    for line in main_help.splitlines():
        if re.match(r"^(  [a-z]|Commands:)", line):
            out.append(line)
    out.append("```\n")

    # Per-subcommand help + examples
    for cmd in subcommands:
        out.append(f"### oline {cmd}\n")
        out.append("```")
        cmd_help = run_help([cmd, "--help"])
        out.append(cmd_help.rstrip() if cmd_help.strip() else f"No detailed help available for {cmd}")
        out.append("```\n")

        # Inject matching examples
        cmd_key = f"oline {cmd}"
        if cmd_key in examples:
            _filename, body = examples[cmd_key]
            out.append("#### Examples\n")
            out.append(body.rstrip())
            out.append("")

        out.append("---\n")

    # Justfile Recipes section — commands starting with "just "
    just_commands = {k: v for k, v in examples.items() if k.startswith("just ")}
    if just_commands:
        out.append("## Justfile Recipes\n")
        # Group by filename to avoid duplicates
        seen_files = set()
        for cmd_key in sorted(just_commands.keys()):
            filename, body = just_commands[cmd_key]
            if filename in seen_files:
                continue
            seen_files.add(filename)
            out.append(f"### {cmd_key}\n")
            out.append(body.rstrip())
            out.append("\n---\n")

    # Footer
    out.append("## Examples\n")
    out.append("For detailed examples of any command, run:\n")
    out.append("```bash")
    out.append("oline <command> --examples")
    out.append("```\n")

    # Write output
    content = "\n".join(out) + "\n"
    OUTPUT.write_text(content)
    line_count = content.count("\n")
    print(f"==> Written: {OUTPUT} ({line_count} lines)")

    # Write index
    write_index(examples)
    print(f"==> Written: {INDEX_FILE}")


if __name__ == "__main__":
    main()
